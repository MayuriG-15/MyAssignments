package week1.assignment2;

public class NegtoPos {

	public static void main(String[] args) {
		int num = -40;
		if(num<0) {
			System.out.println("This is Negative Number: "+num);
			int posNum = -(num);
			System.out.println("The number "+num+ " is converted into Positive Number: "+posNum);
		}

	}

}
